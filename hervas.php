        <h2 class="h2TituloArticulos">El Barrio Judío</h2>
        <p> Este barrio es conocido por sus callejuelas estrechas y empinadas, llenas de casas con grandes voladizos
            hechas de materiales autóctonos, que bajan desde la Plaza hasta el río Ambroz y la Fuente
            Chiquita.<br /><br />

            La comunidad hebrea se estableció en Hervás alrededor del siglo XIV, aunque no hay registros documentales
            hasta el siglo XV. Estaba compuesta por unas 45 familias que eran propietarias de varios edificios
            importantes, incluyendo una Casa de la Cofradía y la Sinagoga. Aunque la Sinagoga original no se conserva,
            la tradición oral la sitúa en la calle Rabilero.<br /><br />

            Con la promulgación del Edicto de Expulsión de los judíos por los Reyes Católicos en 1492, muchas familias
            judías se exiliaron a Portugal o se convirtieron al cristianismo. Los judíos de Hervás se dedicaban
            principalmente a oficios artesanales como zapateros, curtidores, sastres y lenceros, y también había
            escribanos y prestamistas.<br /><br />

            El Barrio Judío de Hervás fue declarado Conjunto Histórico Artístico en 1969, y pertenece a la Red de
            Juderías de España “Caminos de Sefarad”. Hoy en día, recorrer este barrio permite disfrutar de la artesanía
            local y de la gastronomía tradicional, especialmente durante la fiesta de Los Conversos, que celebra la
            historia sefardí de la localidad.</p>
